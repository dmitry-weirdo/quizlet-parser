def is_descriptive_or_term(answer: str) -> bool:
    if answer and answer[0].islower():
        return True
    verbs = (
        "играл", "ехали", "занимались", "делал", "сделал", "был", "была",
        "были", "есть", "нет", "может", "должен", "переводится", "называется",
    )
    low = answer.lower()
    if any(low.startswith(v + " ") or f" {v} " in f" {low} " for v in verbs):
        return True
    if any(m in low for m in TERM_MARKERS):
        return True
    if low.endswith(("ение", "ание", "ость", "ство", "изм", "ист", "ция", "тия")):
        if answer not in LANGUAGES and len(answer.split()) <= 4:
            return True
    if re.search(r'["«»]', answer) and len(answer.split()) >= 2:
        return True
    return False


def looks_like_person_name(answer: str) -> bool:
    words = answer.split()
    if not 1 <= len(words) <= 4:
        return False
    if not all(w and w[0].isupper() for w in words):
        return False
    if re.search(r"[.!?«»\"]", answer):
        return False
    return True


def is_idiom_or_quote(answer: str) -> str | None:
    words = WORD_RE.findall(answer)
    if len(words) >= 6:
        if re.search(r"[.!?…]$", answer) or "?" in answer:
            return "IDIOM" if len(words) >= 8 else "QUOTE"
        if answer[0].isupper() and len(words) >= 10:
            return "QUOTE"
    if len(answer) > 60 and ("," in answer or ";" in answer):
        return "QUOTE"
    if answer.count(" ") >= 5 and re.search(r"[.!?]$", answer):
        return "IDIOM"
    return None


def is_song(answer: str) -> bool:
    if answer in SONGS:
        return True
    if re.search(r"^[A-Z][a-zA-Z\s.'\-,]+$", answer) and len(answer.split()) >= 2:
        if any(c in answer for c in ".!'"):
            return True
    if re.fullmatch(r"[A-Z][A-Z.]+", answer.replace(" ", "")):
        return True
    return False


def guess_personalia_gender(answer: str) -> str | None:
    if answer in PERSONALIA_FEMALE:
        return "PERSONALIA_FEMALE"
    words = answer.split()
    if not words:
        return None
    first = words[0].lower().rstrip(".")
    if first in FEMALE_FIRST:
        return "PERSONALIA_FEMALE"
    last = words[-1].lower()
    if any(last.endswith(e) for e in FEMALE_SURNAME_ENDINGS):
        if not any(last.endswith(e) for e in ("ский", "цкий", "ич")):
            return "PERSONALIA_FEMALE"
    if len(words) >= 2 and any(last.endswith(e) for e in MALE_SURNAME_ENDINGS):
        return "PERSONALIA_MALE"
    if len(words) == 1 and last.endswith(("ий", "ей", "ай", "он", "ан", "ен")):
        if answer in GODS:
            return None
        return "PERSONALIA_MALE"
    if len(words) >= 2:
        return "PERSONALIA_MALE"
    return None


def classify(answer: str) -> str | None:
    a = answer.strip()
    if not a:
        return None

    if a in NICKNAMES:
        return "NICKNAME"

    if is_composite(a):
        return None
    if is_descriptive_or_term(a):
        return None
    if "(" in a or len(a.split()) > 5:
        return None

    if re.fullmatch(r"\d{4}", a):
        y = int(a)
        if 1000 <= y <= 2100:
            return "YEAR"
    if DATE_RE.match(a):
        return "DATE"
    if NUMBER_RE.fullmatch(a):
        return "NUMBER"

    idiom = is_idiom_or_quote(a)
    if idiom:
        return idiom

    if a in LANGUAGES:
        return "LANGUAGE"
    if a in BRANDS:
        return "BRAND"
    if a in SONGS or is_song(a):
        return "SONG"
    if a in ENGRAVINGS:
        return "ENGRAVING"
    if a in PAINTINGS:
        return "PAINTING"
    if a in MOVIES:
        return "MOVIE"

    if a in COUNTRIES:
        return "COUNTRY"
    if a in US_STATES:
        return "STATE"
    if a in CITIES:
        return "CITY"

    if a in GODDESSES:
        return "GODDESS"
    if a in GODS:
        return "GOD"

    if a in CHARACTER_FEMALE:
        return "CHARACTER_FEMALE"
    if a in CHARACTER_MALE:
        return "CHARACTER_MALE"

    if a in PERSONALIA_FEMALE:
        return "PERSONALIA_FEMALE"

    pg = guess_personalia_gender(a)
    if pg:
        if pg == "PERSONALIA_MALE" and a in GODDESSES:
            return "GODDESS"
        if pg == "PERSONALIA_FEMALE" and a in GODS:
            return "GOD"
        return pg

    # nickname: short metaphorical or "ключ к" style
    if 2 <= len(a.split()) <= 5 and not looks_like_person_name(a):
        nick_markers = ("прозвище", "ключ", "отец", "мать", "король", "королева")
        low = a.lower()
        if any(m in low for m in nick_markers):
            return "NICKNAME"
    if looks_like_person_name(a):
        pg = guess_personalia_gender(a)
        if pg:
            return pg
        # single capitalized word without clear surname → unmapped
        if len(a.split()) == 1:
            return None
        # multi-word: require surname-like last word for male default
        last = a.split()[-1].lower()
        if any(last.endswith(e) for e in MALE_SURNAME_ENDINGS + FEMALE_SURNAME_ENDINGS):
            return "PERSONALIA_MALE"
        return None

    if re.search(r"^[A-Z][a-z]+(\s+[A-Z][a-z]+)+$", a) and len(a.split()) <= 3:
        return "BRAND"

    return None


def build_outputs(answers: list[str], total_cards: int) -> None:
    by_type: dict[str, list[str]] = {t: [] for t in TYPES}
    unmapped: list[str] = []

    for a in answers:
        t = classify(a)
        if t:
            by_type[t].append(a)
        else:
            unmapped.append(a)

    for t in TYPES:
        by_type[t].sort(key=lambda x: x.lower())

    unmapped.sort(key=lambda x: x.lower())

    per_type = {t: len(by_type[t]) for t in TYPES}
    mapped = sum(per_type.values())
    stats = {
        "total_cards": total_cards,
        "unique_answers": len(answers),
        "mapped": mapped,
        "unmapped": len(unmapped),
        "mapped_percent": round(100 * mapped / len(answers), 1) if answers else 0,
        "per_type": per_type,
        "source_files": sorted(p.name for p in MODULES.glob("*.txt")),
    }

    ROOT.mkdir(parents=True, exist_ok=True)
    (ROOT / "by-type.json").write_text(
        json.dumps(by_type, ensure_ascii=False, indent=2), encoding="utf-8"
    )
    (ROOT / "unmapped.json").write_text(
        json.dumps(unmapped, ensure_ascii=False, indent=2), encoding="utf-8"
    )
    (ROOT / "stats.json").write_text(
        json.dumps(stats, ensure_ascii=False, indent=2), encoding="utf-8"
    )

    # top unmapped by frequency in source
    freq: Counter[str] = Counter()
    for path in MODULES.glob("*.txt"):
        for line in path.read_text(encoding="utf-8").splitlines():
            if "\t" in line:
                ans = re.sub(r"\s+", " ", line.split("\t", 1)[0].strip())
                if ans in unmapped:
                    freq[ans] += 1
    top_unmapped = freq.most_common(30)

    readme = f"""# Сущности из quizlet-modules по типам

Уникальные **ответы** (левая колонка) из [`quizlet-modules/`](../../quizlet-modules/), сгруппированные по типам из [`text_parsing_guide.md`](../../docs/text_parsing_guide.md).

## Статистика

| Метрика | Значение |
|---------|----------|
| Карточек (строк с TAB) | {total_cards} |
| Уникальных ответов | {len(answers)} |
| Сопоставлено с типом | {mapped} ({stats['mapped_percent']}%) |
| Unmapped | {len(unmapped)} |

### По типам

| Тип | Кол-во |
|-----|--------|
"""
    for t in TYPES:
        if per_type[t]:
            readme += f"| `{t}` | {per_type[t]} |\n"

    readme += """
## Методология

- Парсинг: `ответ<TAB>вопрос`; многострочные вопросы игнорируются (берётся только строка с TAB).
- Классификация **только по тексту ответа**, без контекста вопроса.
- При сомнении ответ попадает в `unmapped.json`.
- Составные ответы (`/`, `A и B`), описательные фразы и общие термины — unmapped.

## Ограничения

- Не 100% разметка: «Филадельфия» может быть городом в одной карточке и контекстом в другой.
- Род/падеж не кодируются — только тип сущности.
- Цель — обучающий корпус для расширения `text_parsing_guide.md`.

## Топ-30 unmapped (по частоте в модулях)

"""
    for ans, cnt in top_unmapped:
        readme += f"- `{ans}` — {cnt} карточ.\n"

    readme += """
## Воспроизведение

```bash
python cursor/entities-from-quizlet-cards/extract_and_classify.py
```
"""
    (ROOT / "README.md").write_text(readme, encoding="utf-8")

    return stats


def main() -> int:
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")

    answers, total_cards = parse_answers()
    stats = build_outputs(answers, total_cards)

    assert stats["mapped"] + stats["unmapped"] == stats["unique_answers"]

    print(
        f"unique: {stats['unique_answers']}, "
        f"mapped: {stats['mapped']}, "
        f"unmapped: {stats['unmapped']} "
        f"({100 - stats['mapped_percent']:.1f}% unmapped)"
    )
    return 0


