def parse_answers() -> tuple[list[str], int]:
    """Return unique answers (order preserved) and total card count."""
    seen: dict[str, None] = {}
    total = 0
    for path in sorted(MODULES.glob("*.txt")):
        for line in path.read_text(encoding="utf-8").splitlines():
            if "\t" not in line:
                continue
            answer = line.split("\t", 1)[0].strip()
            if not answer:
                continue
            total += 1
            answer = re.sub(r"\s+", " ", answer)
            if answer not in seen:
                seen[answer] = None
    return list(seen.keys()), total


def is_composite(answer: str) -> bool:
    if "/" in answer:
        return True
    if re.search(r"\s+и\s+", answer, re.I):
        parts = re.split(r"\s+и\s+", answer, flags=re.I)
        if len(parts) >= 2 and all(len(p.split()) <= 4 for p in parts):
            return True
    return False


